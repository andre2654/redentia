        <p>
          Seu cérebro está cablado para segurar perdedores e vender vencedores. Shefrin e Statman batizaram o fenômeno em 1985 de disposition effect. Custo médio documentado em literatura comportamental: 3,2% a 5,7% de retorno anual perdido pela má sequência de vendas.
        </p>

        <h3>Os 5 gatilhos quantitativos que destravam a venda</h3>

        <p>
          Os três primeiros são quantitativos, sai da emoção. Os dois últimos são comportamentais.
        </p>

        <ol>
          <li><strong>Valuation:</strong> Múltiplo 25% acima da média histórica de 5 anos. Se entrou em P/L 8x e hoje está em 18x, parte da venda já é matemática.</li>
          <li><strong>Concentração:</strong> Qualquer ação acima de 20% da carteira pede venda parcial. Pergunte ao acionista da Light, OGX ou Americanas o que custou ignorar essa regra.</li>
          <li><strong>Tese:</strong> O motivo pelo qual comprou não vale mais. Comprou Vale por ciclo de commodity? Se o ciclo virou e você manteve, não é tese. É inércia.</li>
          <li><strong>Macro:</strong> Os três indicadores do capítulo anterior piscam juntos. Em cem anos isso aconteceu quatro vezes, e o S&P caiu pelo menos 35% nos cinco anos seguintes em todas.</li>
          <li><strong>Insônia:</strong> Se você não dorme com a posição, sua carteira está errada para você. Vender 30% reduz a dor. Vender 100% restaura a clareza.</li>
        </ol>

